/* Aula 03 "Arquivo JS Operadores e Operacões" */
let n1 = 10 // number inteiro
let n2 = 2 // number inteiro
let n3 = "2" // string texto

document.write(`N1 ${n1} <br>`)
document.write(`N2 ${n2} <br>`)
document.write(`N3 ${n3} <br>`)

console.log(n1 + n2) // adição
console.log(n1 - n2) // subtração
console.log(n1 * n2) // multiplicação
console.log(n1 / n2) // divisão
console.log(n1 % n2) // resto da divisão / módulo.

console.log(n1 == n2) // igual a 
console.log(n1 === n2) // idêntico a
console.log(n1 != n2) // diferente de, Não =
console.log(n1 !== n2) // Não é identico

console.log(n1 > n2) // maior que
console.log(n1 < n2) // menor que
console.log(n1 >= n2) // maior ou igual a
console.log(n1 <= n2) // menor ou igual a

let a = true
let b = false
console.log(a && b) // E AND
console.log(a || b) // OU OR
console.log(!a) // Não not negação
console.log(!b) // Não not negação

